#Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrada.
import math
numero = float(input('Entre com o valor desejado: ')) 

print(f'Dobro do número desejado = {numero * 2}')
print(f'Triplo do número desejado = {numero * 3}')
print(f'Valor da raiz quadrada do número desejado = {math.sqrt(numero)}')